#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	size_t	size;
	size_t	i;
	char	*res;

	if (!s || !f)
		return (NULL);
	size = 0;
	while (s[size])
		size++;
	if (!(res = (char *)ft_memalloc((size + 1) * sizeof(char))))
		return (NULL);
	i = 0;
	while (s[i])
	{
		res[i] = f(i, s[i]);
		i++;
	}
	res[i] = 0;
	return (res);
}
